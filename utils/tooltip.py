# ── 툴팁 HTML 생성 ────────────────────────────────────────────────────────────

def tt(key: str, display: str | None = None) -> str:
    """CSS 말풍선 툴팁 HTML 반환."""
    term = TERMS.get(key)
    label = display or key
    if not term:
        return label

    emoji  = term.get("emoji", "")
    title  = term.get("title", key)
    body   = term.get("body", "")
    ex     = term.get("example", "")
    effect = term.get("effect", "")

    ex_html     = f'<div class="tt-example">📌 {ex}</div>'   if ex     else ""
    effect_html = f'<div class="tt-effect">{effect}</div>'   if effect else ""

    return (
        f'<span class="tt">{label}'
        f'<span class="tt-box">'
        f'<div class="tt-title">{emoji} {title}</div>'
        f'<div class="tt-body">{body}</div>'
        f'{ex_html}'
        f'{effect_html}'
        f'</span></span>'
    )


def help_text(key: str) -> str:
    """st.metric(help=...) 용 순수 텍스트."""
    term = TERMS.get(key)
    if not term:
        return ""
    body   = term.get("body", "")
    ex     = term.get("example", "")
    effect = term.get("effect", "")
    parts  = [body]
    if ex:
        parts.append(f"📌 {ex}")
    if effect:
        parts.append(f"🔗 연관 영향\n{effect}")
    return "\n\n".join(parts)
